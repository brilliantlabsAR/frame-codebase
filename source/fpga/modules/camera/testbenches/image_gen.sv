                    else if (x_counter[1] && !y_counter[0]) pixel_data_out <= 'h3ff; //g
                    else if (!x_counter[0] && y_counter[0]) pixel_data_out <= 'h3ff; //g
                    else if (x_counter[0] && y_counter[0]) pixel_data_out <= 'h0; //r
                end

