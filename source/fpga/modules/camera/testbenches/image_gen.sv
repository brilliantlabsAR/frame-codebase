                    pixel_data_red_out <= 'h3ff;
                    pixel_data_green_out <= 'h00;
                    pixel_data_blue_out <= 'h3ff;

                    if (!x_counter[0] && !y_counter[0]) pixel_data_out <= 'h3ff; //b
